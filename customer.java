public class customer {
    int id;
    String name;
    String pass;
    int age;
    char gender;

    customer(int id,
    String name,
    String pass,
    int age,
    char gender) {
        this.id = id;
        this.name = name;
        this.pass = pass;
        this.age = age;
        this.gender = gender;
    }
}
